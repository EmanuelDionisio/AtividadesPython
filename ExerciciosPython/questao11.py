"""Faça um algoritmo para ler três números e exibir a soma
do número 1 com o número 2, multiplicado pela soma do
número 2 pelo 3."""
a1 = int(input("Digite o numero: "))
a2 = int(input("Digite outro numero: "))
a3 = int(input("Digite mais outro numero: "))
print("O resultado é: ",(a1 + a2) * (a2 + a3))